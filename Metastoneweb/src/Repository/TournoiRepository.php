    public function findByPriceRange($minValue,$maxValue)
    {
        return $this->createQueryBuilder('tournoi')
            ->andWhere('tournoi.recompence >= :minVal')
            ->setParameter('minVal', $minValue)
            ->andWhere('tournoi.recompence <= :maxVal')
            ->setParameter('maxVal', $maxValue)
            ->orderBy('tournoi.idTournoi', 'ASC')
            ->setMaxResults(10)
            ->getQuery()
            ->getResult()
        ;
